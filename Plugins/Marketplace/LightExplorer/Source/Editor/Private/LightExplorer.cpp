template<typename T>
T* GetCDO(UBlueprint* Blueprint) {
	if (Blueprint==nullptr) {return nullptr;}
	//
	const UClass* BlueprintClass = Blueprint->GeneratedClass;
	return Cast<T>(BlueprintClass->ClassDefaultObject);
}

